public class Tutor {
	private String nome, cpf, email, telefone, dataCadastro;
	
	public Tutor(String nome, String cpf, String email, String telefone, String dataCadastro) {
		this.nome = nome;
		this.cpf = cpf;
		this.email = email;
		this.telefone = telefone;
		this.dataCadastro = dataCadastro;
	}
}